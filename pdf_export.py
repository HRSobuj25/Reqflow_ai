def _add_cover_flowables(story: list, styles: dict, project_name: str, industry: str, doc_type: str, timestamp: str) -> None:
    story.append(Spacer(1, 1.2 * inch))
    story.append(Paragraph("ReqFlow AI  |  ERP Requirement Engine", styles["CoverBrand"]))
    story.append(Paragraph(doc_type.upper(), styles["CoverSub"]))
    
    # Large Title
    story.append(HRFlowable(width="60%", thickness=1.5, color=colors.HexColor(_HEX_PURPLE), spaceAfter=15))
    story.append(Paragraph(project_name, styles["CoverTitle"]))
    story.append(HRFlowable(width="60%", thickness=1.5, color=colors.HexColor(_HEX_PURPLE), spaceAfter=20))
    
    # Metadata details
    story.append(Paragraph(f"Industry Domain: {industry}", styles["CoverMeta"]))
    story.append(Paragraph(f"Generated: {timestamp}", styles["CoverTime"]))
    
    story.append(Spacer(1, 1 * inch))
    story.append(Paragraph(
        "CONFIDENTIAL — For internal use only.<br/>"
        "Generated automatically by ReqFlow AI Enterprise.",
        styles["CoverFoot"]
    ))
    story.append(PageBreak())


