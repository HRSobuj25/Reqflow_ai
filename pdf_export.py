def build_pdf_document(
    project_name: str,
    industry: str,
    content: str,
    doc_label: str,
    timestamp: Optional[str] = None
) -> bytes:
    """
    Builds a single-section PDF document.
    """
    ts = timestamp or datetime.now().strftime("%B %d, %Y at %H:%M")
    styles = _get_pdf_styles()
    
    pdf_buffer = io.BytesIO()
    doc = SimpleDocTemplate(
        pdf_buffer,
        pagesize=letter,
        leftMargin=54,
        rightMargin=54,
        topMargin=54,
        bottomMargin=54
    )
    
    story = []
    _add_cover_flowables(story, styles, project_name, industry, doc_label, ts)
    _add_meta_table_flowable(story, styles, project_name, industry, doc_label, ts)
    
    # Parse Markdown content
    _append_markdown_to_story(story, content, styles)
    
    # End note
    story.append(Spacer(1, 15))
    story.append(Paragraph(
        f"<i>— End of {doc_label} — {project_name} — Generated by ReqFlow AI — {ts}</i>",
        styles["CoverFoot"]
    ))
    
    doc.build(story, canvasmaker=NumberedCanvas)
    pdf_buffer.seek(0)
    return pdf_buffer.read()


def build_complete_suite_pdf(
    project_name: str,
    industry: str,
    timestamp: Optional[str],
    sections: dict[str, str]
) -> bytes:
    """
    Builds a comprehensive ERP Requirement Suite PDF containing all available sections.
    """
    ts = timestamp or datetime.now().strftime("%B %d, %Y at %H:%M")
    styles = _get_pdf_styles()
    
    pdf_buffer = io.BytesIO()
    doc = SimpleDocTemplate(
        pdf_buffer,
        pagesize=letter,
        leftMargin=54,
        rightMargin=54,
        topMargin=54,
        bottomMargin=54
    )
    
    story = []
    _add_cover_flowables(story, styles, project_name, industry, "Complete ERP Requirement Suite", ts)
    
    # Render Table of Contents Page
    story.append(Paragraph("Table of Contents", styles["CustomH1"]))
    story.append(Spacer(1, 10))
    
    idx = 1
    for key, val in _SECTION_META.items():
        content = sections.get(key, "")
        if content and content.strip() and not content.startswith("*"):
            title = val[0]
            story.append(Paragraph(f"{idx}. &nbsp; {title}", styles["TOCItem"]))
            idx += 1
            
    story.append(Spacer(1, 20))
    story.append(HRFlowable(width="100%", thickness=1, color=colors.HexColor(_HEX_PURPLE), spaceAfter=15))
    _add_meta_table_flowable(story, styles, project_name, industry, "Complete ERP Requirement Suite", ts)
    story.append(PageBreak())
    
    # Render sections
    active_keys = [k for k, v in _SECTION_META.items() if sections.get(k) and not sections[k].startswith("*")]
    
    for i, key in enumerate(active_keys):
        title, code = _SECTION_META[key]
        content = sections[key]
        
        # Section Header
        story.append(HRFlowable(width="100%", thickness=1.5, color=colors.HexColor(_HEX_PURPLE), spaceAfter=10))
        story.append(Paragraph(f"[{code}] &nbsp; {title}", styles["CustomH1"]))
        story.append(Spacer(1, 10))
        
        _append_markdown_to_story(story, content, styles)
        
        # Page break if not final section
        if i < len(active_keys) - 1:
            story.append(PageBreak())
            
    # Final footer flowable
    story.append(Spacer(1, 20))
    story.append(Paragraph(
        f"<i>— End of Complete ERP Requirement Suite — {project_name} — Generated by ReqFlow AI — {ts}</i>",
        styles["CoverFoot"]
    ))
    
    doc.build(story, canvasmaker=NumberedCanvas)
    pdf_buffer.seek(0)
    return pdf_buffer.read()
