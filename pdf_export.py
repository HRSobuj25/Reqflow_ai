# ===========================================================================
#  Numbered Canvas for Running Headers and Footers
# ===========================================================================
class NumberedCanvas(canvas.Canvas):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._saved_page_states = []

    def showPage(self):
        self._saved_page_states.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        num_pages = len(self._saved_page_states)
        for state in self._saved_page_states:
            self.__dict__.update(state)
            self.draw_page_decorations(num_pages)
            super().showPage()
        super().save()

    def draw_page_decorations(self, total_pages: int):
        if self._pageNumber == 1:
            # Skip header/footer on cover page
            return

        self.saveState()
        self.setFont("Helvetica", 8)
        self.setFillColor(colors.HexColor(_HEX_BODY))
        
        # Draw header
        self.drawString(54, 750, "ReqFlow AI  |  Enterprise ERP Requirement Suite")
        self.setStrokeColor(colors.HexColor(_HEX_BORDER))
        self.setLineWidth(0.5)
        self.line(54, 742, 558, 742)

        # Draw footer
        self.drawString(54, 45, "CONFIDENTIAL — Generated by ReqFlow AI")
        page_str = f"Page {self._pageNumber} of {total_pages}"
        self.drawRightString(558, 45, page_str)
        self.restoreState()


