def _parse_inline_markdown(text: str) -> str:
    """Safely converts simple markdown (bold, code) into HTML tags for Paragraph."""
    # Escape existing HTML brackets to prevent parsing issues
    text = text.replace("<", "&lt;").replace(">", "&gt;")
    # Bold **text**
    text = re.sub(r"\*\*([^*]+)\*\*", r"<b>\1</b>", text)
    # Code `text`
    text = re.sub(r"`([^`]+)`", r"<font face='Courier' color='#4F46E5'>\1</font>", text)
    return text


def _flush_pdf_table(story: list, table_lines: list, body_style: ParagraphStyle, header_style: ParagraphStyle) -> None:
    parsed_rows = []
    for line in table_lines:
        # Skip divider rows (e.g. |---|---|)
        if re.match(r"^\|[\s\-:|]+\|$", line.strip()):
            continue
        cells = [c.strip() for c in line.strip().strip("|").split("|")]
        parsed_rows.append(cells)
        
    if not parsed_rows:
        return
        
    cols_count = max(len(r) for r in parsed_rows)
    table_data = []
    
    for r_idx, row in enumerate(parsed_rows):
        formatted_row = []
        for c_idx in range(cols_count):
            cell_text = row[c_idx] if c_idx < len(row) else ""
            html_text = _parse_inline_markdown(cell_text)
            if r_idx == 0:
                p = Paragraph(f"<b>{html_text}</b>", header_style)
            else:
                p = Paragraph(html_text, body_style)
            formatted_row.append(p)
        table_data.append(formatted_row)

    # Let each column take an equal slice of the page printable area (approx 500pt width)
    col_width = 504.0 / cols_count
    t = Table(table_data, colWidths=[col_width] * cols_count)
    t.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, 0), colors.HexColor(_HEX_TH_BG)),
        ('ALIGN', (0, 0), (-1, -1), 'LEFT'),
        ('VALIGN', (0, 0), (-1, -1), 'TOP'),
        ('INNERGRID', (0, 0), (-1, -1), 0.5, colors.HexColor(_HEX_BORDER)),
        ('BOX', (0, 0), (-1, -1), 1, colors.HexColor(_HEX_BORDER)),
        ('TOPPADDING', (0, 0), (-1, -1), 6),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 6),
        ('LEFTPADDING', (0, 0), (-1, -1), 8),
        ('RIGHTPADDING', (0, 0), (-1, -1), 8),
    ]))
    
    story.append(t)
    story.append(Spacer(1, 10))


def _append_markdown_to_story(story: list, markdown: str, styles: dict) -> None:
    lines = (markdown or "").splitlines()
    in_code = False
    table_buf = []

    for raw in lines:
        line = raw.rstrip()

        # Handle Code Fences
        if re.match(r"^```", line):
            if in_code:
                in_code = False
                story.append(Spacer(1, 6))
            else:
                if table_buf:
                    _flush_pdf_table(story, table_buf, styles["CustomBodyText"], styles["TableHeader"])
                    table_buf.clear()
                in_code = True
            continue

        if in_code:
            # Code line
            story.append(Paragraph(f"<font face='Courier'>{line}</font>", styles["CodeBlock"]))
            continue

        # Handle Tables
        if re.match(r"^\|(.+)\|$", line):
            table_buf.append(line)
            continue
        else:
            if table_buf:
                _flush_pdf_table(story, table_buf, styles["CustomBodyText"], styles["TableHeader"])
                table_buf.clear()

        # Horizontal rules
        if re.match(r"^[-_*]{3,}$", line.strip()):
            story.append(Spacer(1, 4))
            story.append(HRFlowable(width="100%", thickness=1, color=colors.HexColor(_HEX_PURPLE), spaceAfter=8))
            continue

        # Headings
        m = re.match(r"^#\s+(.+)$", line)
        if m:
            story.append(Paragraph(m.group(1), styles["CustomH1"]))
            continue

        m = re.match(r"^##\s+(.+)$", line)
        if m:
            story.append(Paragraph(m.group(1), styles["CustomH2"]))
            continue

        m = re.match(r"^###\s+(.+)$", line)
        if m:
            story.append(Paragraph(m.group(1), styles["CustomH3"]))
            continue

        m = re.match(r"^####\s+(.+)$", line)
        if m:
            story.append(Paragraph(m.group(1), styles["CustomH4"]))
            continue

        # Lists (Bullets and Numbers)
        m = re.match(r"^[-*\u2022]\s+(.+)$", line)
        if m:
            html = _parse_inline_markdown(m.group(1))
            story.append(Paragraph(f"&bull; &nbsp; {html}", styles["CustomBullet"]))
            continue

        m = re.match(r"^(\d+)\.\s+(.+)$", line)
        if m:
            num = m.group(1)
            html = _parse_inline_markdown(m.group(2))
            story.append(Paragraph(f"{num}. &nbsp; {html}", styles["CustomNumber"]))
            continue

        # Blank lines
        if not line.strip():
            story.append(Spacer(1, 6))
            continue

        # Paragraphs
        html = _parse_inline_markdown(line)
        story.append(Paragraph(html, styles["CustomBodyText"]))

    # Flush remaining table
    if table_buf:
        _flush_pdf_table(story, table_buf, styles["CustomBodyText"], styles["TableHeader"])


